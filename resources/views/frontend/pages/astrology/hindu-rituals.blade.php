            <h2>Various Rituals Celebrated In India</h2>
            <p><b>Chhati Ceremony:</b> This ceremony is celebrated when the new-born is six days old. On occasion, a priest is invited who perform a religious ceremony for the purification of the infant. It is also believed that God writes his or her destiny according to his deeds in his or her last birth.</p>
            <p><b>Annaprashana:</b> This ceremony is celebrated to feed the newly born with cooked food when he or she is six to eight months old. For this purpose, a priest is called who chooses an auspicious date for this ceremony. It is organized for the good health and growth of the baby.</p>
            <p><b>Mundan:</b> This ceremony is organized to get the baby’s birth hair shaved off when he or she is between four months to three years old. For this purpose, a barber is called to perform this ceremony. Parents also organize a feast on this day.</p>
            <p><b>Shraddha:</b> This is the first death anniversary, celebrated for the departed soul. On this day, a priest is called who performs a ritual so that his or her soul could rest in peace. Dakshina (money) and alms are given to the priest and the needy.</p>
